import Foundation

struct AppCornerRadius {
    // Standard system-like corner radius
    let small: CGFloat = 4
    let medium: CGFloat = 8
    let large: CGFloat = 12

    // Cards
    let card: CGFloat = 8

    // Buttons, inputs - Standard rounded rects, not pills
    let button: CGFloat = 8
    let input: CGFloat = 8
    let pill: CGFloat = 8

    // Sidebar items
    let sidebarItem: CGFloat = 6
}
